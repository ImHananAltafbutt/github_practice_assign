def get_student_grade():
    """
    Prompts the user for a student's score and prints their corresponding grade.
    """
    try:
        score = float(input("Enter the student's score (0-100): "))

        if 0 <= score <= 100:
            if score >= 90:
                print("Result: Grade A - Excellent!")
            elif score >= 80:
                print("Result: Grade B - Very Good!")
            elif score >= 70:
                print("Result: Grade C - Good.")
            elif score >= 60:
                print("Result: Grade D - Pass.")
            else:
                print("Result: Grade F - Fail.")
        else:
            print("Invalid score. Please enter a score between 0 and 100.")
    except ValueError:
        print("Invalid input. Please enter a numeric score.")

# Call the function to run the program
get_student_grade()

